public class Yuja {
	
	private int yuja;
	private int sugar;
	
	//setter	
	public void setYuja(int y) {
		yuja=y;
	}
	public void setSugar(int s) {
		sugar=s;
	}
	
	//getter
	public int getYuja() {
		return yuja;
	}
	public int getSugar() {
		return sugar;
	}
}//////////////////////////////////////////////
